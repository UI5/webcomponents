/**
 * Sort order for Timeline items.
 * @public
 * @since 2.8.0
 */
enum TimelineSortOrder {
	/**
	 * No sorting applied.
	 * @public
	 */
	None = "None",

	/**
	 * Ascending order (oldest first).
	 * @public
	 */
	Ascending = "Ascending",

	/**
	 * Descending order (newest first).
	 * @public
	 */
	Descending = "Descending",
}

export default TimelineSortOrder;
