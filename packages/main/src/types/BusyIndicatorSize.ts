/**
 * Different BusyIndicator sizes.
 * @public
 */
enum BusyIndicatorSize {
	/**
	 * automatic size (component decides)
	 * @public
	 * @since 2.17.0
	 */
	Auto = "Auto",

	/**
	 * small size
	 * @public
	 */
	S = "S",

	/**
	 * medium size
	 * @public
	 */
	M = "M",

	/**
	 * large size
	 * @public
	 */
	L = "L",
}

export default BusyIndicatorSize;
